public class ContohIfElse {
    public static void main(String[] args) {
        String nilai = "pulpena";

        if (nilai == ("hp")) {
            System.out.println("saya ingin hp");
        } else if (nilai == ("pulpen")) {
            System.out.println("saya ingin pulpen");
        } else {
            System.out.println("saya ingin id card");
        }
    }
}

